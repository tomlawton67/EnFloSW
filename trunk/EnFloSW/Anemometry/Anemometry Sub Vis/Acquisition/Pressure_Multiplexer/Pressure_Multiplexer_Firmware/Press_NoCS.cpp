#include "Press_NoCS.h"


void clsPress_NoCS::Initialise()
{
  SPI.begin();
  memset(&Buffer_Full[0], 0, L_Buffer_Full);
  memset(&Buffer_Fast[0], 0, L_Buffer_Fast);
}


void clsPress_NoCS::GetSample_Full() // Gets entire 4 byte data, P + T
{
  SPI.beginTransaction(spiPress);

  //  digitalWriteFast(Pin_CS, LOW);
  //Delay_2500ns(); // Required according to timing spec
  SPI.transfer(Buffer_Full, 4); // P MSB (includes stale bits), P LSB, T MSB, T LSB
  //  digitalWriteFast(Pin_CS, HIGH);
  //  delayMicroseconds(2); // Required according to timing spec, but not necessary in present application (never going to poll this fast!)

  SPI.endTransaction();
}


void clsPress_NoCS::GetSample_Fast() // Gets only 2 byte pressure data P
{
  SPI.beginTransaction(spiPress);

  //  digitalWriteFast(Pin_CS, LOW);
  //Delay_2500ns(); // Required according to timing spec
  SPI.transfer(Buffer_Fast, 2); // P MSB (includes stale bits), P LSB
  //  digitalWriteFast(Pin_CS, HIGH);
  //  delayMicroseconds(2); // Required according to timing spec, but not necessary in present application (never going to poll this fast!)

  SPI.endTransaction();
}


float clsPress_NoCS::CalcTemperature()
{
  float T = (float)(word(Buffer_Full[2], Buffer_Full[3]) >> 5) * 0.097703956 - 50.0;
  return T;
}


byte clsPress_NoCS::ExtractStatusBits_fromFull()
{
  byte S = (Buffer_Full[0] & 0b11000000) >> 6;
  return S;
}


byte clsPress_NoCS::ExtractStatusBits_fromFast()
{
  byte S = (Buffer_Fast[0] & 0b11000000) >> 6;
  return S;
}


